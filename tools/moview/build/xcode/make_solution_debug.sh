#! /bin/bash

PROJECT=moview
CONFIGURATION=Debug
SOLUTION_TYPE=XCODE
SOLUTION_DIR=../solutions/$PROJECT_$SOLUTION_TYPE_$CONFIGURATION

pushd ..
mkdir -p $SOLUTION_DIR
pushd $SOLUTION_DIR
/Applications/CMake.app/Contents/bin/cmake -G "Xcode" "${PWD}/../.." -DCMAKE_CONFIGURATION_TYPES:STRING=$CONFIGURATION -DCMAKE_BUILD_TYPE:STRING=$CONFIGURATION -D$SOLUTION_TYPE:BOOLEAN=ON
popd
popd
